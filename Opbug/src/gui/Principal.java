    private void creaza_BMP(String pdatos) {
        try {
            BarcodePDF417 pdf417 = new BarcodePDF417();
            pdf417.setText(pdatos);
            Image rawImage = pdf417.createAwtImage(Color.BLACK, Color.WHITE);
            BufferedImage outImage = new BufferedImage(rawImage.getWidth(null), rawImage.getHeight(null), 1);
            outImage.getGraphics().drawImage(rawImage, 0, 0, null);
             JOptionPane.showMessageDialog(null, "Pdatos este: " + pdatos, "Informare", 1);
            File f = new File("coduri_bara.bmp");
            if (f.exists()) {
                f.delete();
            }
            ImageIO.write((RenderedImage)outImage, "bmp", f);
            JOptionPane.showMessageDialog(null, "Informare", "fisier bmp generat1(datos)", 1);
        }
        catch (IOException ex) {
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
        } 
        JOptionPane.showMessageDialog(null, "Informare", "Fisier bmp generat", 1);
      }
    
