    private void txt_cif_plFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txt_cif_plFocusLost
       if (this.txt_cif_pl.isEnabled() && this.txt_cif_pl.isEditable()) {
            try {
                double cpl = Double.parseDouble(this.txt_cif_pl.getText());
                if (cpl <= 0.0) {
                    JOptionPane.showMessageDialog(null, "cod fiscal platitor incorect, trebuie sa fie numeric", "Eroare", 0);
                    this.txt_cif_pl.requestFocus();
                }
            }
            catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "cod fiscal platitor incorect, trebuie sa fie numeric", "Eroare", 0);
                this.txt_cif_pl.requestFocus();
                return;
            }
            int vcui = Main.verif_cui(this.txt_cif_pl.getText());
            if (vcui != 0) {
                JOptionPane.showMessageDialog(null, "Eroare", "cod fiscal platitor incorect", 0);
                this.txt_cif_pl.requestFocus();
            }
        }
    }//GEN-LAST:event_txt_cif_plFocusLost

